package less_than_Operator_Example;

public class JavaExample {

	public static void main(String[] args) {
		int num1 = 10, num2 = 20, num3 = 5;
		
		//checking whether num1 is less than num2 using '<' operator
		System.out.println("num1 is less than num2: " + (num1<num2));
		
		//checking whether num1 is less than num3 using '<' operator
		System.out.println("num1 is less than num3: " + (num1<num3));
	}

}
