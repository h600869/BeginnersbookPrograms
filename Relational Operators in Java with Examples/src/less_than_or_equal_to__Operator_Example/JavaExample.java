package less_than_or_equal_to__Operator_Example;

public class JavaExample {

	public static void main (String args[]) {
		int x = 100, y = 150, z = 100;
		
		//checking whether x is less than or equal to y
		System.out.println("x is less than or equal to y: " + (x<=y));
		
		//checking whether x is less than or equal to z
		System.out.println("x is less than or equal to z: " + (x<=z));
	}
}
