package equal_to_Operator_Example;

public class JavaExample {

	public static void main(String[] args) {
		int num1 = 9, num2 = 9;
		
		// = = Operator is used to check whether num1 and num2 are equal or not
		if (num1 == num2) {
			System.out.println("Numbers num1 and num2 are equal");
		}
		else {
			System.out.println("Numbers num1 and num2 are not equal");
		}
	}

}
