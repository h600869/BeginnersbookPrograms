package greater_Than_Operator_Example;

public class JavaExample {

	public static void main(String[] args) {
		int num1 =10, num2 = 20, num3 = 5;
		
		//Checking whether num1 > num2
		System.out.println("num1 is greater than num2: " + (num1>num2));
		
		//Checking whether num1> num3
		System.out.println("num1 is greater than num3: " + (num1>num3));
	}

}
